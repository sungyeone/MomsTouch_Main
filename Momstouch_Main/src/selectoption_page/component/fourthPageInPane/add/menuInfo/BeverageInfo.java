package selectoption_page.component.fourthPageInPane.add.menuInfo;

public class BeverageInfo {
	
	String beverageName;
	int price;
	String beveragePath;
	
	public String getBeverageName() {
		return beverageName;
	}
	public void setBeverageName(String beverageName) {
		this.beverageName = beverageName;
	}
	public int getPrice() {
		return price;
	}
	public void setPrice(int price) {
		this.price = price;
	}
	public String getBeveragePath() {
		return beveragePath;
	}
	public void setBeveragePath(String beveragePath) {
		this.beveragePath = beveragePath;
	}
}
