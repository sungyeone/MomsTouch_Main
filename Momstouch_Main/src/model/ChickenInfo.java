package model;

public class ChickenInfo {
	
	private int chicken_code;
	private String chicken_name;
	private int chicken_price;
	private String chicken_image_link;
	private String chicken_image_link2;
	private String chicken_info;
	
	
	public int getChicken_code() {
		return chicken_code;
	}
	public void setChicken_code(int chicken_code) {
		this.chicken_code = chicken_code;
	}
	public String getChicken_name() {
		return chicken_name;
	}
	public void setChicken_name(String chicken_name) {
		this.chicken_name = chicken_name;
	}
	public int getChicken_price() {
		return chicken_price;
	}
	public void setChicken_price(int chicken_price) {
		this.chicken_price = chicken_price;
	}
	public String getChicken_image_link() {
		return chicken_image_link;
	}
	public void setChicken_image_link(String chicken_image_link) {
		this.chicken_image_link = chicken_image_link;
	}
	public String getChicken_image_link2() {
		return chicken_image_link2;
	}
	public void setChicken_image_link2(String chicken_image_link2) {
		this.chicken_image_link2 = chicken_image_link2;
	}
	public String getChicken_info() {
		return chicken_info;
	}
	public void setChicken_info(String chicken_info) {
		this.chicken_info = chicken_info;
	}
	
	
	
}
