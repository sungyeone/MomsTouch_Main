package model;

public class NewMenuInfo {
	
	private int new_menu_code;     
	private String new_menu_name;        
	private int new_menu_price;          
	private String new_menu_link;           
	private String new_menu_link2;
	
	
	public int getNew_menu_code() {
		return new_menu_code;
	}
	public void setNew_menu_code(int new_menu_code) {
		this.new_menu_code = new_menu_code;
	}
	public String getNew_menu_name() {
		return new_menu_name;
	}
	public void setNew_menu_name(String new_menu_name) {
		this.new_menu_name = new_menu_name;
	}
	public int getNew_menu_price() {
		return new_menu_price;
	}
	public void setNew_menu_price(int new_menu_price) {
		this.new_menu_price = new_menu_price;
	}
	public String getNew_menu_link() {
		return new_menu_link;
	}
	public void setNew_menu_link(String new_menu_link) {
		this.new_menu_link = new_menu_link;
	}
	public String getNew_menu_link2() {
		return new_menu_link2;
	}
	public void setNew_menu_link2(String new_menu_link2) {
		this.new_menu_link2 = new_menu_link2;
	}
	
	
	
}
